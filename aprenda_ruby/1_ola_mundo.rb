# Isso é um comentário, quando você coloca um # no começo da linha
# o ruby ignora esta linha.

# Mostrando um string. Lembre-se string é o tipo que guarda palavras
puts "Olá Ruby seu lindo!!"

# Fazendo contas
puts 3 + 3

# Mais contas
puts 4 - 2
puts 5 * 2
puts 10 / 5

# Colocando valor em uma variável
n = 6 - 3

# Mostrando o valor
puts n

# Colocando string numa variável
nome = "Ingrid"

# Concatenando strings com variáveis
puts "Olá, meu nome é " + nome  + ". Prazer em conhecê-lo!"

# Interpolando strings. Dentro do #{} você coloca código ruby
# neste caso o valor da variável nome vai aparecer.
puts "Olá, meu nome é #{nome}. Prazer em conhecê-lo!"

# Funciona com ambos strings e numeros
puts "Qual é meu numero? #{n}. Muito bom!"
